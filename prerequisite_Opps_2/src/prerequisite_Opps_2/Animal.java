package prerequisite_Opps_2;

public class Animal {
	String gender;
	int age;
	int weight;
	
	
	public Animal(String gender, int age, int weight) {
		super();
		this.gender = gender;
		this.age = age;
		this.weight = weight;
	}
		
		public eat()
		{
			System.out.println("eating......");
		}
		
		public void sleep()
		{
			System.out.println("sleeping......");
		}
	
	
}
